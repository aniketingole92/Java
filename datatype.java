// Primitive data types
byte smallNumber = 100;          // 8-bit integer
short mediumNumber = 1000;       // 16-bit integer
int number = 100000;             // 32-bit integer (most common)
long bigNumber = 1000000000L;    // 64-bit integer

float decimal = 3.14f;           // 32-bit floating point
double preciseDecimal = 3.14159; // 64-bit floating point (most common)

char letter = 'A';               // Single 16-bit Unicode character
boolean flag = true;             // true or false

// Reference data types
String greeting = "Hello";       // Sequence of characters
